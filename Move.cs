public class Move 
{
    public int val;
    public int x;
    public int y;
    public int matrix_x;
    public int matrix_y;


    public Move(int val, int x, int y, int matrix_x, int matrix_y){
        this.val = val;
        this.x = x;
        this.y = y;
        this.matrix_x = matrix_x;
        this.matrix_y = matrix_y;
  }
}